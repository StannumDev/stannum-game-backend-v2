// Helper: flatten nested children resources into a flat array with parentId
// ---------------------------------------------------------------------------
const flattenResources = (resources) => {
    const flat = [];
    for (const res of resources) {
        const { children, ...rest } = res;
        flat.push({ ...rest, parentId: null });
        if (children && children.length > 0) {
            for (const child of children) {
                const { children: _grandchildren, ...childRest } = child;
                flat.push({ ...childRest, parentId: rest.id });
            }
        }
    }
    return flat;
};

